/**
 * HAProxy ACL/sample fetch method definitions.
 *
 * Source: HAProxy 3.1 configuration.txt section 7.3 (Fetching samples)
 * and section 7.4 (Pre-defined ACLs).
 *
 * These are the criteria used in "acl" definitions and in conditions after
 * actions (e.g. "http-request deny if { ... }").
 *
 * Categories:
 *   L4  — available from tcp-request connection rules onward
 *   L5  — available from tcp-request session rules onward
 *   L6  — buffer content inspection (tcp-request content)
 *   L7  — HTTP-only (requires mode http or HTTP upgrade)
 *   INT — internal / environment fetch methods
 */

export type FetchLayer = 'L4' | 'L5' | 'L6' | 'L7' | 'internal';
export type FetchOutputType = 'boolean' | 'integer' | 'ip' | 'string' | 'binary';

export interface FetchMethodDef {
  readonly name: string;
  readonly signature: string;
  readonly description: string;
  readonly outputType: FetchOutputType;
  readonly layer: FetchLayer;
  readonly since: string;
  /** ACL-specific declinations (name variants for pattern matching) */
  readonly aclDeclinations?: readonly string[];
}

/** ACL flags usable in any "acl" declaration. */
export interface AclFlagDef {
  readonly flag: string;
  readonly description: string;
}

